type EnergyLevel = 'high' | 'medium' | 'low'

async function importHistory() {
  const sql = neon(process.env.DATABASE_URL!)
  const db = drizzle(sql)

  // 실제 유저
  const allUsers = await db.select().from(users)
  const realUser = allUsers.find((u) => u.name !== null) ?? allUsers[0]
  console.log(`User: ${realUser.name} (${realUser.id})`)

  // 습관 ID 매핑
  const userHabits = await db.select().from(habits).where(eq(habits.userId, realUser.id))
  const habitMap = new Map<string, { id: string; energyLevel: string }>()
  for (const h of userHabits) {
    habitMap.set(h.name, { id: h.id, energyLevel: h.energyLevel })
  }
  console.log('Habits:', [...habitMap.keys()].join(', '))

  // 날짜별로 완료한 습관 모으기
  const dateMap = new Map<string, string[]>() // date -> habitId[]
  for (const [name, dates] of Object.entries(HISTORY)) {
    const habit = habitMap.get(name)
    if (!habit) {
      console.log(`WARNING: habit "${name}" not found, skipping`)
      continue
    }
    for (const date of dates) {
      const existing = dateMap.get(date) ?? []
      existing.push(habit.id)
      dateMap.set(date, existing)
    }
  }

  // 에너지 레벨 결정: 완료 습관 중 가장 높은 에너지 기준
  const energyRank: Record<string, number> = { high: 3, medium: 2, low: 1 }
  const rankToEnergy: Record<number, EnergyLevel> = { 3: 'high', 2: 'medium', 1: 'low' }

  function pickEnergy(completedIds: string[]): EnergyLevel {
    let maxRank = 1
    for (const id of completedIds) {
      const habit = [...habitMap.values()].find((h) => h.id === id)
      if (habit) {
        maxRank = Math.max(maxRank, energyRank[habit.energyLevel] ?? 1)
      }
    }
    return rankToEnergy[maxRank] ?? 'medium'
  }

  // 기존 시드 데이터 삭제 (3/7 제외)
  const existingLogs = await db.select().from(dailyLogs).where(eq(dailyLogs.userId, realUser.id))
  for (const log of existingLogs) {
    if (log.date !== '2026-03-07') {
      await db.delete(dailyLogs).where(eq(dailyLogs.id, log.id))
    }
  }
  console.log(`Cleared ${existingLogs.length - 1} old logs (kept 3/7)`)

  // 삽입
  const sortedDates = [...dateMap.keys()].sort()
  let count = 0
  for (const date of sortedDates) {
    const completedIds = dateMap.get(date)!
    const energyLevel = pickEnergy(completedIds)

    await db.insert(dailyLogs).values({
      id: nanoid(),
      userId: realUser.id,
      date,
      energyLevel,
      completedHabitIds: JSON.stringify(completedIds),
    })
    count++
  }

  console.log(`\nInserted ${count} daily logs (${sortedDates[0]} ~ ${sortedDates[sortedDates.length - 1]})`)
  console.log('Done! Cmd+Shift+R')
}

importHistory().catch(console.error)
